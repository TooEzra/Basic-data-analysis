#numeric data types
num1 = 55
num2 = 5.3
print(num1)
print(num2)

#list data type
languages = ["Python", "Dart", "Web", 23]
print(languages)

#list items
languages = ["Python", "Dart", "Web", 23]
print(languages[1]) # prints Dart
print(languages[2]) # prints Web
print(languages[3])  # prints 23
print(languages[0])  # prints Python

#tuple data type
products = ('XBox', 499.99, "Habibi", 23)
print(products)

#access tuple
products = ('XBox', 499.99, "Habibi", 23)
print(products[2])
print(products[0])
print(products[1])  # prints 499.99
print(products[3])  # prints 23

#set data type
student_ids = {112, 114, 117, 113}
print(student_ids)

#dictionary data type
capital_city = {"Kenya": "Nairobi", "Nigeria": "Lagos"}
print(capital_city)